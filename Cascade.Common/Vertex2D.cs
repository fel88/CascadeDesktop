using System.Reflection;

namespace Cascade.Common
{
    public class Vertex2D
    {
        public Vertex2D() { }
        public Vertex2D(double x, double y)
        {
            X = x;
            Y = y;
            
        }
        public double X { get; set; }
        public double Y { get; set; }
        
    }
    
}
